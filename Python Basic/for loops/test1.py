arr = [1,2,3,4]
for x in arr:
    print (x, '\n')
    
    
# looping through a string
for x in "stewart":
    print (x, '\n')
    
# break statement: used to exit the loop
for x in "stewart":
    if x == "w":
        break
    print (x)
    
# the range function: used to generate a sequence of numbers
for x in range(5):
    print (x, '\n')